package com.wuren.datacenter.bean;

public class CameraInfoBean {
	
	private String name;
	
	private String IP;
	
	private String port;
	
	private String model;
	
	private String sn;
	
	private String smartcenter_sn;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIP() {
		return IP;
	}

	public void setIP(String iP) {
		IP = iP;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getSn() {
		return sn;
	}

	public void setSn(String sn) {
		this.sn = sn;
	}

	public String getSmartcenter_sn() {
		return smartcenter_sn;
	}

	public void setSmartcenter_sn(String smartcenter_sn) {
		this.smartcenter_sn = smartcenter_sn;
	}



}
